template<typename STR>
std::string to_vecstr(const STR& str) {
	std::string s;

	for(size_t i=0; i < str.size(); ++i) {
		s += "[" + std::to_string(i) + ",'" + str.at(i) + "'], ";
	}

	if(s.empty() == false) {
		s.pop_back();
		s.pop_back();
	}

	return s;
}

